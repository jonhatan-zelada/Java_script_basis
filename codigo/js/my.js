function suma(n1,n2,n3,n4,n5){
    var resultado;
    resultado = n1 + n2 +n3 + n4 + n5 ;
    return resultado;
}

function calcularImpuesto(impuesto,ingreso){
    var resultado;
    resultado = impuesto * ingreso;
    return resultado;
}

function promedio(n1,n2,n3){
    var resultado;
    resultado = (n1+n2+n3)/3;
    return resultado;
}

